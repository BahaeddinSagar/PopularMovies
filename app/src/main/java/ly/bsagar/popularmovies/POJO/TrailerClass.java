package ly.bsagar.popularmovies.POJO;

public class TrailerClass {
    String key;
    String name;

    public TrailerClass(String key, String name) {
        this.key = key;
        this.name = name;
    }

    public String getKey() {
        return key;
    }

    public String getName() {
        return name;
    }
}
